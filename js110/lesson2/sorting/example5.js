/*
-JavaScritpt String use UTF-16 code points. Not to be mistaken for ASCII
-UTF stands for Unicode Transformations Format

- UTF-16 code can be found using Sring.prototype.charCodeAt()

General Rules To Understand

- Uppercase letters come before LowerCase letters
  'A' = 65  'a' = 97

- Digits and most punctuation come before letters

- There is an extended ASCII table

- All other UTF-16 characters come after the extended ASCII table and have a code point of at least 256

*/